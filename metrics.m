% --- Executes on button press in deger1.

function deger1_Callback(hObject, eventdata, handles)

[filename,filepath]=uigetfile({'*.*'},'ANALÝZ EDÝLECEK RESMÝ SEÇÝNÝZ');
selectedImage=imread(strcat(filepath,filename));
axes(handles.axes1);
imshow(selectedImage);

A = imnoise(selectedImage,'salt & pepper', 0.02);
axes(handles.axes3);
imshow(A);

%%%%%%%%%%%%
err = immse(A, selectedImage);
set(handles.text2,'String',err);

%%%%%%%%%%%%
[peaksnr, snr] = psnr(A, selectedImage);
set(handles.text3,'String',peaksnr);
set(handles.text4,'String',snr);

%%%%%%%%%%%%
H = fspecial('Gaussian',[11 11],1.5);
B = imfilter(selectedImage,H,'replicate');
[ssimval] = ssim(B,selectedImage);
set(handles.text5,'String',ssimval);

%%%%%%%%%%%%%%%%%%%%%%%%%
brisqueInoise = brisque(A);
set(handles.text13,'String',brisqueInoise);
%%%%%%%%%%%
brisqueInoise1 = brisque(selectedImage);
set(handles.text6,'String',brisqueInoise1);

%%%%%%%%%%%%%%%%%%%%%%
niqeInoise = niqe(A);
set(handles.text14,'String',niqeInoise);
%%%%%%%%%%%%%%%%%%%%%%
niqeInoise1 = niqe(selectedImage);
set(handles.text8,'String',niqeInoise1);

